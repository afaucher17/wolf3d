#include "libft.h"

int		ft_match(char *s1, char *s2)
{
	if (*s2 == '*')
	{
		if (!*s1)
		{
			while (*s2 == '*')
				s2++;
			return (*s2 == '\0');
		}
		if (*(s2 + 1) == '\0')
			return (1);
		return (ft_match(s1 + 1, s2) || ft_match(s1, s2 + 1));
	}
	if (*s1 == *s2 || *s2 == '?')
	{
		if (!*s1 && !*s2)
			return (1);
		if (*s1)
			return (ft_match(s1 + 1, s2 + 1));
	}
	return (0);
}
